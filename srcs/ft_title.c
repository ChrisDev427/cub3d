#include "cub3d.h"

void	ft_title(t_game *game)
{
	int	x;
	int	y;

	y = 0;
	x = 0;
	while (y < 35)
	{
		while (x < SCREEN_WIDTH)
		{
			ft_my_pixel_put(&game->image.title, x, y, 0x181818);
			x++;
		}
		y++;
		x = 0;
	}
}
